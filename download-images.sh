# Clean up any existing PNG files
rm -f public/images/landing/*.png

# Verify downloads
echo "Verifying downloaded images..."
for file in public/images/landing/*.jpg; do
    if [ -f "$file" ]; then
        size=$(stat -f%z "$file")
        if [ "$size" -gt 1000 ]; then
            echo "✓ $file downloaded successfully ($size bytes)"
        else
            echo "✗ $file is too small ($size bytes) - download failed"
            rm "$file"
        fi
    else
        echo "✗ $file not found"
    fi
done

# Make the script executable
chmod +x download-images.sh 